int		ft_strcmp(char *s1, char *s2)
{
	int a;

	a = 0;
	while (s1[a] || s2[a])
	{
		if (s1[a] < s2[a])
			return (-1);
		if (s1[a] > s2[a])
			return (1);
		a++;
	}
	return (0);
}
